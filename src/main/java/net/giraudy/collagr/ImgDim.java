package net.giraudy.collagr;

/**
 *
 * @author etiennegiraudy
 */
public class ImgDim {

    private int height;
    private int width;

    public ImgDim(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
            
}
